#include<stdio.h>
int main(void)
{
	int a = 4;
	switch (a)
	{
	case 1:
		printf("it's 1\n");
		break;
	case 2:
		printf("it's 2\n");
		break;
	case 3:
		printf("it's 3\n");
		break;
	case 4:
		printf("it's 4\n");
		break;
	case 5:
		printf("it's 5\n");
		break;
	default:
		printf("it's 0\n");
		break;
	}
}